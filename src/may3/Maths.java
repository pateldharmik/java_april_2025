package may3;

public class Maths {
    public static void main(String[] args) {

        int num1 = 20;
        int num2 = 18;

        int sum = num1 + num2;
        System.out.println("The sum of the 2 numbers :" + sum);

        int sub = num1 - num2;
        System.out.println("The subtraction of the 2 numbers :" + sub);

        int mul = num1 * num2;
        System.out.println("Multiplication of the 2 numbers is: " + mul);

        int div = num1 / num2;
        System.out.println("Division of the 2 numbers are: " + div);

        int mod = num1 % num2;
        System.out.println("Modulo of the 2 numbers are: " + mod);

    }
}
